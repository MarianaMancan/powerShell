clear-host
[char]$letra = Read-Host "Digite uma letra: "
if($letra -eq 'a'){
"A letra é uma vogal"
}
elseif($letra -eq 'e'){
"A letra é uma vogal"
}
elseif($letra -eq 'i'){
"A letra é uma vogal"
}
elseif($letra -eq 'o'){
"A letra é uma vogal"
}
elseif($letra -eq 'u'){
"A letra é uma vogal"
}
else{
"Letra é consoante"
}
